import React, { useEffect, useState } from 'react';

interface ToastProps {
    type: "success" | "danger" | "warning";
    message: string;
    onClose?: () => void;
}

const Toast = ({ type, message, onClose }: ToastProps) => {
    const [isVisible, setIsVisible] = useState(true); // Step 2

    const typeSettings = {
        success: {
            bgColor: "bg-green-500",
            textColor: "text-green-200",
            iconPath: "M10 .5a9.5 9.5 0 1 0 9.5 9.5A9.51 9.51 0 0 0 10 .5Zm3.707 8.207-4 4a1 1 0 0 1-1.414 0l-2-2a1 1 0 0 1 1.414-1.414L9 10.586l3.293-3.293a1 1 0 0 1 1.414 1.414Z",
        },
        danger: {
            bgColor: "bg-red-500",
            textColor: "text-red-200 ",
            iconPath: "M10 .5a9.5 9.5 0 1 0 9.5 9.5A9.51 9.51 0 0 0 10 .5Zm3.707 11.793a1 1 0 1 1-1.414 1.414L10 11.414l-2.293 2.293a1 1 0 0 1-1.414-1.414L8.586 10 6.293 7.707a1 1 0 0 1 1.414-1.414L10 8.586l2.293-2.293a1 1 0 0 1 1.414 1.414L11.414 10l2.293 2.293Z",
        },
        warning: {
            bgColor: "bg-orange-500",
            textColor: "text-orange-200",
            iconPath: "M10 .5a9.5 9.5 0 1 0 9.5 9.5A9.51 9.51 0 0 0 10 .5ZM10 15a1 1 0 1 1 0-2 1 1 0 0 1 0 2Zm1-4a1 1 0 0 1-2 0V6a1 1 0 0 1 2 0v5Z",
        },
    };

    const { bgColor, textColor, iconPath } = typeSettings[type] || typeSettings.success;

    // Step 4: Conditional rendering based on isVisible

    // set timeout to hide the toast


    if (!isVisible) return null;

    const handleClose = () => {
        setIsVisible(false);
        onClose && onClose();
    }

    return (
        <div className="w-full h-full fixed top-0 left-0 bg-black bg-opacity-50 z-50">
            <div className="fixed top-1/2 left-1/2 transform -translate-x-1/2 -translate-y-1/2">
                <div className={`flex items-center w-full max-w-xs p-4 mb-4 text-gray-500 bg-white rounded-lg shadow`} role="alert">
                    <div className={`inline-flex items-center justify-center flex-shrink-0 w-8 h-8 ${textColor} ${bgColor} rounded-lg`}>
                        <svg className="w-5 h-5" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="currentColor" viewBox="0 0 20 20">
                            <path d={iconPath} />
                        </svg>
                        <span className="sr-only">Icon</span>
                    </div>
                    <div className="ms-3 text-sm font-bold mr-4">{message}</div>
                    <button type="button" className="ms-auto -mx-1.5 -my-1.5 bg-white text-gray-400 hover:text-gray-900 rounded-lg focus:ring-2 focus:ring-gray-300 p-1.5 hover:bg-gray-100 inline-flex items-center justify-center h-8 w-8" aria-label="Close"
                        onClick={handleClose}>
                        <span className="sr-only">Close</span>
                        <svg className="w-3 h-3" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 14 14">
                            <path stroke="currentColor" strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="m1 1 6 6m0 0 6 6M7 7l6-6M7 7l-6 6" />
                        </svg>
                    </button>
                </div>
            </div>
        </div>
    );
};

export default Toast;